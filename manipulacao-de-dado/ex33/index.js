// 37. Receba a base e a altura de um retângulo digitados pelo usuário. Em seguida, calcule e exiba o
// perímetro do retângulo.
var baseRetangulo = Number(prompt("BASE: "));
var alturaRetangulo = Number(prompt("ALTURA: "));
var perimetro = 2 * (baseRetangulo + alturaRetangulo);
console.log("PERIMETRO DO RETANGULO É: " + perimetro);
