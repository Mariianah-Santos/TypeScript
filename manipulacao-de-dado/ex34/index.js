// 38. Solicite ao usuário que insira três números decimais. Em seguida, calcule e exiba a média
// aritmética desses números.
var nu1 = Number(prompt("NUMERO 1 (DECIMAL): "));
var nu2 = Number(prompt("NUMERO 2 (DECIMAL): "));
var nu3 = Number(prompt("NUMERO 3 (DECIMAL): "));
var media = (nu1 + nu2 + nu3) / 3;
console.log("A MEDIA É: " + media);
